    {
        if($request->ajax()) {
       
            $data = Agendapunt::whereDate('start', '>=', $request->start)
                      ->whereDate('end',   '<=', $request->end)
                      ->get(['id', 'title', 'start', 'end']);
 
            return response()->json($data);
        }

        return View('agenda.index');
    }

